const CollapsibleSection = ({ id, title, icon: Icon, children, defaultOpen = false }: any) => {
  const [isOpen, setIsOpen] = useState(defaultOpen)

  return (
    <motion.div
      className="bg-zinc-900/50 rounded-xl border border-zinc-800 overflow-hidden"
      initial={{ opacity: 0, y: 20 }}
      whileInView={{ opacity: 1, y: 0 }}
      viewport={{ once: true }}
      transition={{ duration: 0.6 }}
    >
      <button
        onClick={() => setIsOpen(!isOpen)}
        className="w-full p-6 flex items-center justify-between hover:bg-zinc-800/50 transition-colors"
        id={id}
      >
        <div className="flex items-center gap-3">
          <Icon className="w-6 h-6 text-orange-500" />
          <h2 className="text-xl font-bold text-left">{title}</h2>
        </div>
        <motion.div animate={{ rotate: isOpen ? 180 : 0 }} transition={{ duration: 0.3 }}>
          <ChevronDown className="w-5 h-5 text-gray-400" />
        </motion.div>
      </button>

      <AnimatePresence>
        {isOpen && (
          <motion.div
            initial={{ height: 0, opacity: 0 }}
            animate={{ height: "auto", opacity: 1 }}
            exit={{ height: 0, opacity: 0 }}
            transition={{ duration: 0.3 }}
            className="border-t border-zinc-800"
          >
            <div className="p-6 prose prose-invert max-w-none">{children}</div>
          </motion.div>
        )}
      </AnimatePresence>
    </motion.div>
  )
}

